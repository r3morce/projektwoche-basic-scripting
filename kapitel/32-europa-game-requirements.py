# Eine andere Gruppe in der Projektwoche hat ein Brettspiel (Europareise) entwickelt und uns um Hilfe gebeten
# Wir sollen ein Programm entwickeln, welches beim Punkte zählen hilft

# Das Programm soll folgendes können:
# Name von Spieler 1 abfragen
# Name von Spieler 2 abfragen
# Der Punktestand beider Spieler ist 12

# Je nach dem was der Spieler auf dem Brett macht kann er Punkte bekommen oder verlieren:
# Reise mit Auto -1
# Reise Zug Bahn -2
# Reise mit Flugzeug -6
# Einfache Frage beantwortet +4
# Normale Frage beantwortet +5
# Schwierige Frage beantwortet +6
# Es soll auch die Möglichkeit geben Punkte neu zu setzen, zur Korrektur bei Fehlern

# Zu dem Namen des Spielers wird auch immer sein Punktestand dargestellt
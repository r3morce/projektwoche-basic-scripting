# Was ist eine Liste?
# Eine Liste eine Sammlung von Werten, wie zum Beispiel die Namen aller Schüler einer Klasse.
# Die Werte der Liste stehen kommagetrennt in eckigen Klammern.
# Das sieht dann so aus:
["Gülcan", "Benjamin", "Jordan"]

# Überlicherweise wird die gesamte Liste in einer Variable gespeichert:
schueler_namen = ["Gülcan", "Benjamin", "Jordan"]
# schueler_namen ist eine Variable, das kennt ihr schon



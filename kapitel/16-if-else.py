# If Else

# Es ist hilfreich Dinge miteinander zu Vergleichen und entsprechend zu reagieren.
# Dazu gehören zwei Teile:
# Der Vergleich selber, wie zum Beispiel:
"Ill Bill" == "1ll Bill"
# Dieser Vergleich passiert zwar wenn der Code ausgeführt wird, bewirkt aber nichts.

# Deshalb wird der Vergleich in eine Abzweigung eingefügt:
# Eine Abzweigung mit Vergleich sieht folgendermaßen aus:
if "Ill Bill" == "1ll Bill":
    # Wenn der name gleich ist, sollte danach eingerückt ein Befehl stehen
    print("Der Name ist gleich")
else:
    # Sonst steht da ein anderer Befehl, auch eingerückt
    print("Der Name ist nicht gleich")


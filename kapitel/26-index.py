# Index
# Es gibt die Möglichkeit in einer Liste auf nur ein bestimmtes Element zuzugreifen.

# Dazu brauchen wir eine Liste
all_agents = ["Astra", "Breach", "Brimstone", "Chamber", "Cypher", "Kett", "KAY/O", "Killjoy", "Neon", "Omen", "Phoenix", "Raze", "Reyna", "Sage", "Skye", "Sova", "Viper", "Yoru"]

# Wenn es um Listen geht fangen Programmiersprachen bei 0 an zu zählen.
# Das heisst, um auf das erste Element in der Liste zuzugreifen, benutzt du den Index 0
erster_agent = all_agents[0]
print(erster_agent)

# Um auf das zweite Element zuzugreifen benutzt du den Index 1
zweiter_agent = all_agents[1]
print(zweiter_agent)

